// --- PARSING & CACHE ---

function parseSpriteString(str) {
  const pixels = new Uint8Array(16 * 16);
  const rows = str.split('\n');
  for (let y = 0; y < 16; y++) {
    const row = rows[y] || '';
    for (let x = 0; x < 16; x++) {
      const ch = row[x];
      if (ch >= '1' && ch <= '4') {
        pixels[y * 16 + x] = parseInt(ch);
      }
      // '.' or anything else = 0 (transparent)
    }
  }
  return pixels;
}

// Cache: Map<"name:rarity", OffscreenCanvas>
const spriteCache = new Map();
// Parsed pixel data: Map<name, Uint8Array>
const parsedSprites = new Map();

function renderSpriteToCanvas(pixels, shades) {
  const c = new OffscreenCanvas(16, 16);
  const ctx = c.getContext('2d');
  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const shade = pixels[y * 16 + x];
      if (shade > 0) {
        ctx.fillStyle = shades[shade - 1];
        ctx.fillRect(x, y, 1, 1);
      }
    }
  }
  return c;
}

export function initSpriteCache() {
  // Parse all sprites
  for (const [name, str] of Object.entries(SPRITE_DATA)) {
    parsedSprites.set(name, parseSpriteString(str));
  }
  // Pre-render all sprite x rarity combos
  for (const [name, pixels] of parsedSprites) {
    for (const [rarity, shades] of Object.entries(RARITY_SHADES)) {
      spriteCache.set(`${name}:${rarity}`, renderSpriteToCanvas(pixels, shades));
    }
  }
}

export function getSpriteCanvas(spriteName, rarityName) {
  return spriteCache.get(`${spriteName}:${rarityName}`) || null;
}

/**
 * Draw a sprite centered at (cx, cy) scaled to `size` px.
 * Returns false if sprite not found (caller should fall back to char).
 */
export function drawSprite(ctx, spriteName, rarityName, cx, cy, size) {
  // Normalize rarity — may be object { name, color } or string
  const rarity = typeof rarityName === 'object' && rarityName !== null ? rarityName.name : rarityName;
  const canvas = spriteCache.get(`${spriteName}:${rarity || 'common'}`);
  if (!canvas) return false;

  const half = size / 2;
  const prevSmoothing = ctx.imageSmoothingEnabled;
  ctx.imageSmoothingEnabled = false;
  ctx.drawImage(canvas, cx - half, cy - half, size, size);
  ctx.imageSmoothingEnabled = prevSmoothing;
  return true;
}

/**
 * Render a single sprite onto a visible <canvas> element for admin UI.
 * Useful for picker swatches and preview.
 */
export function renderSpriteToElement(canvasEl, spriteName, rarityName, drawSize) {
  const rarity = typeof rarityName === 'object' && rarityName !== null ? rarityName.name : rarityName;
  const cached = spriteCache.get(`${spriteName}:${rarity || 'common'}`);
  if (!cached) return false;

  const ctx = canvasEl.getContext('2d');
  ctx.clearRect(0, 0, canvasEl.width, canvasEl.height);
  ctx.imageSmoothingEnabled = false;
  const ox = (canvasEl.width - drawSize) / 2;
  const oy = (canvasEl.height - drawSize) / 2;
  ctx.drawImage(cached, ox, oy, drawSize, drawSize);
  return true;
}

